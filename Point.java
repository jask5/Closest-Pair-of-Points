/**
 * Point class for passing x and y co-ordinates value and giving them index.
 * Author Jaskirat Kaur
 */
public class Point {
	
	double x;
	double y;
	int pointIndex;
	public Point(double x, double y, int pointIndex) {
		super();
		this.x = x;
		this.y = y;
		this.pointIndex = pointIndex;
	}
	
}
